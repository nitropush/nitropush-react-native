/**
 * Expo config plugin — wires an Android `network_security_config.xml` so
 * the example app can talk to NitroPush dev servers over HTTP.
 *
 * Behaviour: by default `cleartextTrafficPermitted="true"` is applied to
 * the base-config so *any* HTTP host works. The `cleartextHosts` list
 * still gets emitted as an explicit `<domain>` entry — useful as a record
 * of "the dev URLs this example app talks to" and a starting point if
 * you later want to flip `permissive: false` to lock things down per
 * domain only.
 *
 * Why not `expo-build-properties` `usesCleartextTraffic: true`?
 * That flag is ignored when `android:networkSecurityConfig` is set. We
 * also want the explicit XML on disk so prod can flip
 * `permissive: false` and inherit the same allow-list.
 *
 * Usage in `app.json`:
 *   "plugins": [
 *     ...
 *     [
 *       "./plugins/with-network-security-config",
 *       {
 *         "permissive": true,
 *         "cleartextHosts": [
 *           "10.0.2.2",
 *           "localhost",
 *           "127.0.0.1",
 *           "192.168.0.141"
 *         ]
 *       }
 *     ]
 *   ]
 *
 * The plugin runs at `expo prebuild`. After tweaking the host list, run
 * `yarn expo prebuild --clean` to regenerate the `android/` folder.
 */

