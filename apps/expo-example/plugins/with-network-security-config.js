function buildXml(hosts, permissive) {
  const domainEntries = hosts
    .filter((h) => typeof h === "string" && h.trim().length > 0)
    .map(
      (h) =>
        `        <domain includeSubdomains="true">${escape(h.trim())}</domain>`
    )
    .join("\n");

  // In permissive mode the base-config allows any cleartext host (matches
  // `usesCleartextTraffic=true` semantics). In strict mode the base-config
  // blocks cleartext globally and only `cleartextHosts` are allowed.
  const baseCleartext = permissive ? "true" : "false";

  // The domain-config block is only meaningful when the base-config is
  // strict (it carves out exceptions). In permissive mode we still emit
  // it so the host list shows up in the file as documentation, but with
  // the same `cleartextTrafficPermitted` value to keep the merge a no-op.
  const domainBlock = domainEntries
    ? `    <domain-config cleartextTrafficPermitted="${baseCleartext}">
${domainEntries}
        <trust-anchors>
            <certificates src="system" />
        </trust-anchors>
    </domain-config>
`
    : "";

  return `<?xml version="1.0" encoding="utf-8"?>
<!--
  Generated by ./plugins/with-network-security-config.js — DO NOT EDIT.
  Edit the host list / permissive flag in app.json plugin options and
  re-run \`yarn expo prebuild --clean\`.
-->
<network-security-config>
    <base-config cleartextTrafficPermitted="${baseCleartext}">
        <trust-anchors>
            <certificates src="system" />
        </trust-anchors>
    </base-config>
${domainBlock}</network-security-config>
`;
}

function escape(value) {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

module.exports = withNetworkSecurityConfig;
